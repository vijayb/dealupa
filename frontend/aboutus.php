	<h2>Our Vision</h2>

	<br>

	Our goal is to act as your personal concierge, finding products, experiences, services and events that speak to who you are as a person. Do you occasionally like to pamper yourself with a mani-pedi? Or maybe you're an adrenaline junkie who loves jumping out of planes with a parachute strapped to your back. Maybe you're just looking for ideas to take your partner out on a date. Tell us about what matters to you, and we'll do the leg work to find things that you'll love.	
	
	<br><br><br>

	<h2>Technology</h2>

	<br>

	We're big nerds at heart. We love to build products that users love and use bleeding edge technology to do it. Dealupa is built using MySQL, Memcache, PHP and JQuery. We host our service at Rackspace and use Amazon's S3 for storage. We're especially enamored of Solr for our search service and PhantomJS for the power it affords us in our crawling infrastructure. All our infrastructure sits upon the Ubuntu 11.10 operating system.

	If you love technology as much as we do, and care about building products that users love, <a href="mailto:founders@dealupa.com" target=_dealupa>join us</a>!
	
