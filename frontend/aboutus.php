	<h2><b>Dealupa brings you the deals and experiences that match your interests.</b></h2>

	<br>

	We love a good deal. The trouble is, with thousands of deals out there now, we're finding it harder and harder to keep up. What started as a couple of deal sites soon became hundreds; before long, our inboxes were filling up with dozens of daily emails every morning. Worse, deals we wanted started getting lost in a flood of deals we didn't want.

	<br><br>

	That's why we put together Dealupa&#8212;a single place to find the best daily deals. We gather deals from across the web and then we use patent-pending DealRank technology to organize deals according to their quality and relevance to you.
	
